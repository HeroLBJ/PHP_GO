<?php
# PHP中除法运算不会自动取整
$x = 100;
$y = 8;
echo "x / y = ".($x/$y)."<br>";

# PHP7+ 整除运算符 intdiv()
echo "x / y = ".intdiv($x,$y)."<br>";

## == 等于,只比较值
## === 绝对等于,比较值也比较类型
## != 不等于
## <> 不等于
## !== 绝对不等于

## and && 与
## or || 或
## xor 异或:如果x和y有且仅有一个为true,则返回true
## ! 非

## 数组运算符
// x+y x和y的集合
// x==y x和y具有相同的键值对,返回true
// x===y x和y具有相同的键值对且顺序类型相同

## PHP7+ ?? 默认值
$c = $a ?? 'b'; // 如果$a是空,则$c = "b"

## 组合比较符
$z = $x <=> $y; // 如果x>y,返回1; 如果x=y,返回0; 如果x<y,返回-1;
